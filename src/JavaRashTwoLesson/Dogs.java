package JavaRashTwoLesson;

public class Dogs {
    String name;
    int height;
    String color;

    public Dogs(String name) {
        this.name = name;
    }

    public Dogs(String name, int height) {
        this.name = name;
        this.height = height;
    }

    public Dogs(String name, int height, String color) {
        this.name = name;
        this.height = height;
        this.color = color;
    }
}
